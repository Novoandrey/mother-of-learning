--    заготовку под будущий статблок игрока (по аналогии с NPC). Сам
--    статблок игрока строится отдельно — сейчас нужен только контейнер.
-- 4. Обновляет default_fields у node_type 'character': добавляет hint
--    для поля stats.
--
-- Идемпотентно: повторный запуск не создаст дубликатов, не перезапишет
-- существующие stats, не тронет уже переименованного Маркуса.

