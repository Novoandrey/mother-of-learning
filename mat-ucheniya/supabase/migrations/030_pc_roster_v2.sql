insert into nodes (campaign_id, type_id, title, fields)
select
  '00000000-0000-0000-0000-000000000001',
  '10000000-0000-0000-0000-000000000001',
  d.title,
  jsonb_build_object(
    'player',      d.player,
    'description', '',
    'stats',       '{}'::jsonb,
    'tags',        d.tags
  )
from desired d
where not exists (
  select 1 from nodes n
   where n.campaign_id = '00000000-0000-0000-0000-000000000001'
     and n.type_id    = '10000000-0000-0000-0000-000000000001'
     and n.title      = d.title
);

-- ─────────────────────────── 3. Backfill empty stats ───────────────────────────

update nodes
   set fields     = fields || jsonb_build_object('stats', '{}'::jsonb),
       updated_at = now()
 where campaign_id = '00000000-0000-0000-0000-000000000001'
   and type_id    = '10000000-0000-0000-0000-000000000001'
   and not (fields ? 'stats');

-- ─────────────────────────── 4. Default fields hint ───────────────────────────

update node_types
   set default_fields = jsonb_build_object(
     'player',      'text',
     'description', 'textarea',
     'stats',       'json',
     'tags',        'tags'
   )
 where campaign_id = '00000000-0000-0000-0000-000000000001'
   and slug       = 'character';
